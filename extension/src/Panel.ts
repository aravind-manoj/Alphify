const chatContainer: HTMLElement = document.getElementById('chat-container') as HTMLElement;
const userInput: HTMLInputElement = document.getElementById('user-input') as HTMLInputElement;
const sendButton: HTMLElement = document.getElementById('send-button') as HTMLElement;
const uploadButton: HTMLElement = document.getElementById('upload-button') as HTMLElement;
const fileInput: HTMLElement = document.getElementById('file-input') as HTMLElement;
const filePreviewContainer: HTMLElement = document.getElementById('file-preview-container') as HTMLElement;
const welcomeSection: HTMLElement = document.querySelector('.welcome-section') as HTMLElement;

let selectedFiles: any = [];
let isFirstMessage: boolean = true;

userInput.addEventListener('input', () => {
  userInput.style.height = 'auto';
  userInput.style.height = userInput.scrollHeight + 'px';
})

userInput.addEventListener('keydown', (e) => {
  if (e.key === 'Enter' && !e.shiftKey) {
    e.preventDefault();
    sendMessage();
  }
})

sendButton.addEventListener('click', sendMessage);

uploadButton.addEventListener('click', () => {
  fileInput.click();
})

fileInput.addEventListener('change', (e: any) => {
  const files = Array.from(e.target.files);
  selectedFiles = [...selectedFiles, ...files];
  updateFilePreview();
})

function updateFilePreview() {
  filePreviewContainer.innerHTML = selectedFiles.map((file: any, index: any) => `
    <div class="file-preview">
      <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 384 512"><!--!Font Awesome Free 6.6.0 by @fontawesome - https://fontawesome.com License - https://fontawesome.com/license/free Copyright 2024 Fonticons, Inc.--><path d="M0 64C0 28.7 28.7 0 64 0L224 0l0 128c0 17.7 14.3 32 32 32l128 0 0 288c0 35.3-28.7 64-64 64L64 512c-35.3 0-64-28.7-64-64L0 64zm384 64l-128 0L256 0 384 128z"/></svg>
      <span class="file-preview-name">${file.name}</span>
      <span class="remove-file"><svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 384 512"><!--!Font Awesome Free 6.6.0 by @fontawesome - https://fontawesome.com License - https://fontawesome.com/license/free Copyright 2024 Fonticons, Inc.--><path d="M342.6 150.6c12.5-12.5 12.5-32.8 0-45.3s-32.8-12.5-45.3 0L192 210.7 86.6 105.4c-12.5-12.5-32.8-12.5-45.3 0s-12.5 32.8 0 45.3L146.7 256 41.4 361.4c-12.5 12.5-12.5 32.8 0 45.3s32.8 12.5 45.3 0L192 301.3 297.4 406.6c12.5 12.5 32.8 12.5 45.3 0s12.5-32.8 0-45.3L237.3 256 342.6 150.6z"/></svg></span>
    </div>
  `).join('');

  document.querySelectorAll('.remove-file').forEach(button => {
    button.addEventListener('click', (e: any) => {
      const index = parseInt(e.target.dataset.index);
      selectedFiles.splice(index, 1);
      updateFilePreview();
    });
  })
}

function addMessage(content: any, isUser: boolean = false) {
  const messageDiv = document.createElement('div');
  messageDiv.className = `message ${isUser ? 'user' : 'assistant'}`;
  messageDiv.innerHTML = `
    <div class="message-avatar">
      ${isUser ? '<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 448 512"><!--!Font Awesome Free 6.6.0 by @fontawesome - https://fontawesome.com License - https://fontawesome.com/license/free Copyright 2024 Fonticons, Inc.--><path d="M224 256A128 128 0 1 0 224 0a128 128 0 1 0 0 256zm-45.7 48C79.8 304 0 383.8 0 482.3C0 498.7 13.3 512 29.7 512l388.6 0c16.4 0 29.7-13.3 29.7-29.7C448 383.8 368.2 304 269.7 304l-91.4 0z"/></svg>' : '<img src="logo.svg" alt="Assistant Logo">'}
    </div>
    <div class="message-content">
      ${isUser ? content : marked.parse(content)}
    </div>`;
  chatContainer.appendChild(messageDiv);
  chatContainer.scrollTop = chatContainer.scrollHeight;

  if (isFirstMessage && isUser) {
    isFirstMessage = false;
    welcomeSection.classList.add('hidden');
  }
}

function sendMessage() {
  const message = userInput.value.trim();
  if (message || selectedFiles.length > 0) {
    let content = message;
    if (selectedFiles.length > 0) {
      content += '\n\nAttached files:\n' + selectedFiles.map((f: any) => `- ${f.name}`).join('\n');
    }
    addMessage(content, true);
    
    setTimeout(() => {
      addMessage('I received your message!' + (selectedFiles.length ? '\n\nI see you\'ve attached some files. In a real implementation, these would be processed here.' : ''))
    }, 1000);
    
    userInput.value = '';
    userInput.style.height = 'auto';
    selectedFiles = [];
    updateFilePreview();
  }
}